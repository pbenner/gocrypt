type ECC struct {
  Curve EllipticCurve
  G     AffinePoint
  N, H  *big.Int
}

func NewECC(p, a, b, x_, y_, n_, h_ *big.Int) ECC {
  curve := NewEllipticCurve(a, b, p)
  x := big.NewInt(0)
  x.Set(x_)
  y := big.NewInt(0)
  y.Set(y_)
  n := big.NewInt(0)
  n.Set(n_)
  h := big.NewInt(0)
  h.Set(h_)
  g := NewAffinePoint(x, y)
  return ECC{curve, g, n, h}
}

func (ecc ECC) Base() AffinePoint {
  return ecc.G.Clone()
}

func (ecc ECC) Eval(a AffinePoint, b *big.Int) AffinePoint {
  if a.IsZero() {
    return ecc.Curve.MulInt(ecc.G, b)
  } else {
    return ecc.Curve.MulInt(a, b)
  }
}

